   -- Демонстрация  п о с т о я н н ы х  функций, значе-
   -- ниями которых являются списки различных типов
   -- *********************************************
   -- (1) Список целых чисел
   -------------------------
   list1:: [Int]
   list1 = [1,2,3,4,1,4]
    
   ------------------------------
   -- (2) Список булевых значений
   ------------------------------
   list2:: [Bool]
   list2 = [True,False,True]
           
   ----------------------
   -- (3) Список символов
   ----------------------
   list3:: [Char]
   list3 = ['s','t','r','i','n','g'] 

   ---------------------------------
   -- (4) Строка как список символов
   ---------------------------------
   list4:: [Char]
   list4 = "string"

   -----------------------------------------------
   -- (5) Список целых чисел в диапазоне с шагом 1
   -----------------------------------------------
   list5:: [Int]
   list5 = [2..7]           -- [2,3,4,5,6,7]

   --------------------------------------------------
   -- (6) Список целых чисел в диапазоне с шагом (-1)
   --------------------------------------------------
   list6:: [Int]
   list6 = [7,6..3]         -- [7,6,5,4,3]

   --------------------------------------------------------
   -- (7) Список вещественных чисел от 3.1 до 7.0 с шагом 1 
   --------------------------------------------------------
   list7:: [Double]
   list7 = [3.1..7.0]  -- [3.1,4.1,5.1,6.1,7.1]
   list7'= [3.9..7.0]  -- [3.9,4.9,5.9,6.9]

   ----------------------------------------------------------
   -- (8) Список вещественных чисел от 0.0 до 1.0 с шагом 0.3
   ----------------------------------------------------------
   list8:: [Float]
   list8 = [0.0,0.3..1.0]   -- [0.0,0.3,0.6,0.9]

   ------------------------------
   -- (9) Список числовых списков
   ------------------------------
   list9:: [[Int]]
   list9 = [[12,2],[2,12],[],[-1,-7,-3],[10]] 
