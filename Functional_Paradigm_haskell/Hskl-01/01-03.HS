          && langv 'S' == "Scheme"
          && langv 'Р' == "Рефал"
          && langv 'R' == "Функциональный язык нам неизвестен"
   -----------------------------------------------------------
   test2 =   day 01 "Января  " 1550 == 1552
          && day 23 "Февраля " 1230 == 1255
          && day 08 "Марта   " 1100 == 1111
          && day 01 "Апреля  " 2007 == 2012
          && day 01 "Сентября"  900 ==  910
   ----------------------------------------
   test3 = day (-24) "Ноября  " 2004 
