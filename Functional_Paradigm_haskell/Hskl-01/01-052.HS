   -- Демонстрация явного преобразования типов при вычислении
   -- значения функции с аргументами разных числовых типов
   -------------------------------------------------------
   f:: Int -> Integer -> Double -> Float
   f x y z = realToFrac (fromInt x*y)/realToFrac z
   -----------------------------------------------
   -- Неудачные тестовые примеры:
   ------------------------------
   test = f 1 3 3.0 == 1.0