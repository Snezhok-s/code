   -- Демонстрация конструирования новой операции, написанной
   -- на языке программирования Curry и дословно переписанной
   -- на язык Haskell 
   ------------------
   infixl 8 -**
   (-**):: Integer -> Integer -> Integer 
   a -** b = if b>=0 
               then accum 1 a b
               else undefined
      where accum x y z | z==0 = x
                        | True = accum aux (y*y) (z `div` 2)
                  where aux = if z `mod` 2==1
                                then x*y
                                else x
