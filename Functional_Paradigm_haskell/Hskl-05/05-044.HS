   import List (inits,tails,nub,sort)

   -- ******************************************
   -- Функция возвращает все префиксы строки str
   ---------------------------------------------
   pref' str | null str = [""]
             | True     = pref' (init str) ++ [str]
   ------------------------------------------------
   -- Функция возвращает все суффиксы строки str
   ---------------------------------------------
   suff' str | null str = [""]
             | True     = [str] ++ suff' (tail str)
   ------------------------------------------------
   -- Функция возвращает все подцепочки цепочки str
   ------------------------------------------------
   allstr' str | null str = []
               | True     = pref' str ++ allstr' (tail str)
   --------------------------------------------------------
   -- Функция возвращает все подцепочки цепочки str
   ---------------------------------------------------
   allSubWords str = nub $ concatMap pref' (suff' str)

   -- ***************************
   -- Неудачные тестовые примеры:
   ------------------------------
   test1 = pref' "asdasd"
   test2 = inits "asdasd"
   ----------------------
   test3 = suff' "asdasd"
   test4 = tails "asdasd"
   ----------------------------
   test5 = nub $ allstr' "abcd"
   test6 = sort $ allSubWords "abcd"
