   -- Нерекурсивная функция, добавляющая элемент
   -- в конец списка
   ------------------------------
   add_el:: Int -> [Int] -> [Int]
   add_el a lst = lst ++ [a]
   -------------------------------------------
   -- Рекурсивная функция, добавляющая элемент 
   -- в конец списка
   -------------------------------
   add_el':: Int -> [Int] -> [Int]
   add_el' a []     = [a]
   add_el' a (x:xs) = x : add_el' a xs

   -- ***************************
   -- Неудачные тестовые примеры:
   ------------------------------------------
   test1 =  add_el 1 []                == [1]
          && add_el 1 [2,-3,4]         == [2,-3,4,1] 
          && add_el (-2000) [-10,2,-3] == [-10,2,-3,-2000]
   -------------------------------------------------------
   test2 = add_el' 1 []                 == [1]
          && add_el' 1 [2,-3,4]         == [2,-3,4,1] 
          && add_el' (-2000) [-10,2,-3] == [-10,2,-3,-2000]
   --------------------------------------------------------
   test = test1 && test2
