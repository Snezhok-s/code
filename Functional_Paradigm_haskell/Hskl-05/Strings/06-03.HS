   -- Построение списка тех атомов из списка атомов LST, ко-
   -- торые в своем имени содержат русские буквы из множест-
   -- ва {А,В,Е,К,М,Н,О,Р,С,Т,Х}, похожие  по  написанию  на
   -- латинские
   ---------------------------
   like:: [String] -> [String]
   like lst | null lst              = []
            | rus_letter (head lst) = head lst : like (tail lst)
            | True                  = like (tail lst)
   --------------------------------------------------
   -- Предикат, возвращающий True, если в имени атома 
   -- встречается одна из следующих русских букв:
   -- А, В, Е, К, М, Н, О, Р, С, Т, Х
   ----------------------------------
   rus_letter:: String -> Bool
   rus_letter w | null w    = False
                | elem (head w) 
                       ['А','В','Е','К','М','Н','О','Р','С','Т','Х']
                            = True
                | otherwise = rus_letter (tail w)
   ----------------------------------------------
   -- Неудачные тестовые примеры:
   ------------------------------------------
   test = like ["APA","РО","ОР","РА","XBOCT"]
                                           == ["РО","ОР","РА"]
       && like ["CTOK","MEX","BEK","HOPA"] == []
