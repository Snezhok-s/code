   -- Демонстрация построения списка всех подслов заданного
   -- слова wrd (допускается повторение подслов)
   ---------------------------------------------
   main wrd | null wrd = [] 
            | True     = v3 (v1 wrd)
   ---------------------------------------------------------
   -- Функция, возвращающая для каждого слова W  списка  LST
   -- подслова, оканчивающиеся на последнюю букву соответст-
   -- вующего слова W
   -------------------------
   v3:: [String] -> [String]
   v3 lst | null lst = []
          | True     = v2 (head lst) ++ v3 (tail lst)
   -------------------------------------------------------
   -- Функция, возвращающая список всех подслов слова WRD,
   -- начинающихся с первой буквы исходного слова WRD
   --------------------------------------------------
   v1:: String -> [String]
   v1 wrd | null (tail wrd) = [wrd]
          | True            = wrd : v1 (init wrd)
   --------------------------------------------------------
   -- Функция, возвращающая список всех подслов слова  WRD,
   -- оканчивающихся на последнюю букву исходного слова WRD
   --------------------------------------------------------
   v2:: String -> [String]
   v2 wrd | null (tail wrd) = [wrd]
          | True            = wrd : v2 (tail wrd)
