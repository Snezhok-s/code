   -- ================================================
   -- Функция, возвращающая число сочетаний из  n эле-
   -- ментов по m с помощью рекуррентного соотношения
   --
   --   m   m     m-1   0
   --  C = C   + C   , C =1
   --   n   n-1   n-1   n
   --
   -- (н е э ф ф е к т и в н ы е  реализации)
   ------------------------------------------
   c:: Integer -> Integer -> Integer
   c n m | n<m || n<0 || m<0 = error "n>m & n,m>0"
         | n==m || m==0      = 1
         | True              = c (n-1) m + c (n-1) (m-1)
   -----------------------------------------------------
   choose n 0 = 1
   choose n k | n<k  = 0
              | n==k = 1
              | True = choose (n-1) (k-1) + choose (n-1) k

   -- ====================================================
   -- Функция, возвращающая число сочетаний из n элементов
   -- по m с помощью рекуррентного соотношения
   -- (эффективная реализация)
   --
   --   m  n    m-1   0
   --  C = - * C   , C =1
   --   n  m    n-1   n
   ----------------------
   binom n 0 = 1
   binom n m | n < m = 0
             | True  = (n * binom (n-1) (m-1)) `div` m

   -- ***************************
   -- Неудачные тестовые примеры:
   ------------------------------
   test1 =   c 64  2 == 2016
          && c  5  3 ==   10
          && c 13  5 == 1287
          && c  4  3 ==    4
          && c  4  2 ==    6
          && c 99 98 ==   99
   ------------------------------
   test2 =   choose 64  2 == 2016 
          && choose  5  3 ==   10 
          && choose 13  5 == 1287 
          && choose  4  3 ==    4 
          && choose  4  2 ==    6 
          && choose 99 98 ==   99 
   ------------------------------
   test3 =   binom 64  2 == 2016 
          && binom  5  3 ==   10 
          && binom 13  5 == 1287 
          && binom  4  3 ==    4 
          && binom  4  2 ==    6 
          && binom 99 98 ==   99 
   -----------------------------
   test4 n m = binom n m
