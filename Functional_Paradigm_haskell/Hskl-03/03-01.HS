  -- =================================================
   -- Реализация с помощью охранных выражений (<=3990)
   ---------------------------------------------------
   fact1:: Integer -> Integer
   fact1 n | n==0 = 1
           | True = n * fact1 (n-1)

   -- ======================================================
   -- Реализация с помощью сопоставления с образцом (<=3988)
   ---------------------------------------------------------
   fact2:: Integer -> Integer
   fact2   0   = 1
   fact2 (n+1) = (n+1) * fact2 n

   -- =======================================
   -- Реализация с использованием альтернатив
   ------------------------------------------
   fact3:: Integer -> Integer
   fact3 n = if n==0 
               then 1 
               else n * fact3 (n-1)

