   -- Демонстрация сложной рекурсии на числовых структурах
   -- на примере вычисления значения функции "91-МакКарти" 
   -- 
   --  F(n) = If n>100
   --           then n-10
   --           else F (F (F (n + 21)))
   -- *********************************
   f91:: Integer -> Integer 
   f91 n | n>100 = n - 10
         | True  = f91 (f91 (f91 (n+21)))

   -- ***************************
   -- Неудачные тестовые примеры:
   ------------------------------
   test1 =   f91  11 == 91 
          && f91  22 == 91 
          && f91  96 == 91 
          && f91  99 == 91
          && f91 101 == 91 
          && f91 102 == 92 
   ------------------------------
   test2 = map (\x -> (x, f91 x))
               [1..150]
