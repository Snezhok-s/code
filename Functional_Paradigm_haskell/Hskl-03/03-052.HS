   -- Демонстрация вычисления значения выражения:
   -- 
   -- sqrt (3 + sqrt (6 +... + (sqrt 99)...))
   --
   -- ********************************

   -- ================================
   -- П е р в ы й  вариант реализации:
   --  a - минимальное число в выражении;
   --  b - максимальное число в выражении
   --------------------------------------
   raman a b | a==b = sqrt a
             | True = sqrt (a + raman (a+3) b)

   -- ================================
   -- В т о р о й  вариант реализации:
   --  n - количество корней выражения;
   --  a - число, корень которого вычисляется при n=1
   --------------------------------------------------
   raman' n a = raman1' n a a
   raman1' n a res | n==1 = sqrt res
                   | True = raman1' (n-1) (a-3) ((a-3) + sqrt res)

   -- ***************************
   -- Неудачные тестовые примеры:
   ------------------------------
   test1 = raman 96 99
   test2 = raman' 2 99
   test3 = sqrt (96 + sqrt 99)
   ---------------------------
   test4 =   test1 == test2
          && test2 == test3
          && abs (raman 3 99 - raman' 33 99) < 0.00001
