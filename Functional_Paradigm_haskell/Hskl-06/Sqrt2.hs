   -- Демонстрация вычисления всех цифр числа, равного
   -- квадратному корню из 2.
   --
   -- Автор: Ефремов Ростислав (СПбГУ, 2015, ноябрь)
   -- **********************************************
   sqrt2 :: [Integer]
   sqrt2 = sqrt2_ [1] 10 1
   -----------------------
   sqrt2_ lst pos prev 
            = prev : (sqrt2_ (newLst) (pos*10) (last newLst))
        where newLst = getFirst (map (\x -> lst ++ [x]) 
                                     (reverse [0..9])) pos
   -------------------------------------------------------
   getFirst (h:lst) pos | x*x < 2*pos*pos = h
                        | True            = getFirst lst pos
       where x = toNum h
   ---------------------------------------------------
   toNum lst = fst $ foldr (\x (y,z) -> (y+x*z, z*10))
                           (0,1)
                           lst

